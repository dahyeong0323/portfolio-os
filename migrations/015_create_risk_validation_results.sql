CREATE TABLE IF NOT EXISTS risk_validation_results (
    risk_validation_id INTEGER PRIMARY KEY AUTOINCREMENT,
    intent_id INTEGER NOT NULL REFERENCES transaction_intents(intent_id),
    policy_version_id INTEGER NOT NULL REFERENCES risk_policy_versions(policy_version_id),
    reconciliation_id INTEGER REFERENCES reconciliation_snapshots(reconciliation_id),
    ledger_status_at_validation TEXT NOT NULL CHECK(ledger_status_at_validation IN ('reconciled','provisional','stale','broken')),
    ledger_snapshot_as_of TEXT NOT NULL,
    ledger_snapshot_digest TEXT,
    validation_status TEXT NOT NULL CHECK(validation_status IN ('passed','rejected','adjusted')),
    action_class TEXT NOT NULL CHECK(action_class IN ('risk_increasing','risk_reducing','correction','override_precheck')),
    requested_quantity DECIMAL_TEXT CHECK(requested_quantity IS NULL OR typeof(requested_quantity) = 'text'),
    requested_notional DECIMAL_TEXT CHECK(requested_notional IS NULL OR typeof(requested_notional) = 'text'),
    approved_quantity DECIMAL_TEXT CHECK(approved_quantity IS NULL OR typeof(approved_quantity) = 'text'),
    approved_notional DECIMAL_TEXT CHECK(approved_notional IS NULL OR typeof(approved_notional) = 'text'),
    max_allowed_notional DECIMAL_TEXT CHECK(max_allowed_notional IS NULL OR typeof(max_allowed_notional) = 'text'),
    currency TEXT NOT NULL CHECK(length(currency) = 3),
    cash_before DECIMAL_TEXT CHECK(cash_before IS NULL OR typeof(cash_before) = 'text'),
    cash_after DECIMAL_TEXT CHECK(cash_after IS NULL OR typeof(cash_after) = 'text'),
    tax_reserve_required DECIMAL_TEXT CHECK(tax_reserve_required IS NULL OR typeof(tax_reserve_required) = 'text'),
    checks_json TEXT NOT NULL DEFAULT '[]',
    failure_reasons_json TEXT NOT NULL DEFAULT '[]',
    warnings_json TEXT NOT NULL DEFAULT '[]',
    created_at TEXT NOT NULL DEFAULT (strftime('%Y-%m-%dT%H:%M:%SZ','now')),
    expires_at TEXT,
    is_superseded INTEGER NOT NULL DEFAULT 0 CHECK(is_superseded IN (0,1))
);
